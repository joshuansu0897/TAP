    private void work() {
        String host = Host.getText().trim();

        try {
            InetAddress address = InetAddress.getByName(host);
            setMessage("IP address: " + address.toString(), new Color(50, 150, 50));
        } catch (java.net.UnknownHostException e) {
            setMessage("Could not find: " + host, new Color(200, 75, 75));
        }
    }

    private void setMessage(String txt, Color c) {
        result.setText(txt);
        result.setForeground(c);
    }

